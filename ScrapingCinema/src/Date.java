
public class Date {
	private String hall;
	private String time;
	private String mode;
	
	public Date (String hall,String time, String mode)
	{
		this.hall = hall;
		this.time = time;
		this.mode = mode;
	}
	
	public String getHall() {
		return hall;
	}
	public void setHall(String hall) {
		this.hall = hall;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getMode() {
		return mode;
	}
	public void setMode(String mode) {
		this.mode = mode;
	}
	
	

	

}
